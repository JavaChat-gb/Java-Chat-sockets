public class MsgData {

	private String sender;
	private String msg;

	public MsgData(String sender, String msg) {
		super();
		this.sender = sender;
		this.msg = msg;
	}

	public String getSender() {
		return sender;
	}

	public String getMsg() {
		return msg;
	}
	
}
